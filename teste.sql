CREATE TABLE USUARIO (
    ID INT, -- Definindo ID como inteiro
    NOME VARCHAR(40), -- Definindo NOME como uma string de até 40 caracteres
    SOBRENOME VARCHAR(30), -- Definindo SOBRENOME como uma string de até 30 caracteres
    CPF NUMERIC(14) -- Definindo CPF como um número com até 14 dígitos
);
